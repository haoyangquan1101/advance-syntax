"""
 打印毛泽东的《沁园春·雪》
"""
def print_qinyuan_chun_xue():
    """
    打印毛泽东的《沁园春·雪》
    """
    poem = [
        "北国风光，千里冰封，万里雪飘。",
        "望长城内外，惟余莽莽；大河上下，顿失滔滔。",
        "山舞银蛇，原驰蜡象，欲与天公试比高。",
        "须晴日，看红装素裹，分外妖娆。",
        "",
        "江山如此多娇，引无数英雄竞折腰。",
        "惜秦皇汉武，略输文采；唐宗宋祖，稍逊风骚。",
        "一代天骄，成吉思汗，只识弯弓射大雕。",
        "俱往矣，数风流人物，还看今朝。"
    ]

    # 打印诗歌标题和内容
    print("《沁园春·雪》")
    print("作者：毛泽东")
    print("-" * 30)
    for line in poem:
        print(line)

    # 调用方法打印诗歌
    print_qinyuan_chun_xue()



